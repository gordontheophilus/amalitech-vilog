CREATE TABLE admins(
  admin_id INT GENERATED BY DEFAULT AS IDENTITY 
    (START WITH 1 INCREMENT BY 1),
  profile_pic VARCHAR(255),
  email VARCHAR(255) UNIQUE NOT NULL,
  phone VARCHAR(255) UNIQUE NOT NULL,
  first_name VARCHAR(255) NOT NULL,
  last_name VARCHAR(255) NOT NULL,
  company VARCHAR(255),
  user_type VARCHAR(10) DEFAULT 'admin',
  password VARCHAR(255) UNIQUE NOT NULL
);

CREATE TABLE hosts(
  host_id INT GENERATED BY DEFAULT AS IDENTITY 
    (START WITH 10 INCREMENT BY 1),
  profile_pic VARCHAR(255),
  email VARCHAR(255) UNIQUE NOT NULL,
  phone VARCHAR(255) UNIQUE NOT NULL,
  first_name VARCHAR(255) NOT NULL,
  last_name VARCHAR(255) NOT NULL,
  company VARCHAR(255),
  user_type VARCHAR(10) DEFAULT 'host'
);

CREATE TABLE guests(
  guest_id INT GENERATED BY DEFAULT AS IDENTITY 
    (START WITH 50 INCREMENT BY 1),
  profile_pic VARCHAR(255),
  email VARCHAR(255) UNIQUE NOT NULL,
  phone VARCHAR(255) UNIQUE NOT NULL,
  first_name VARCHAR(255) NOT NULL,
  last_name VARCHAR(255) NOT NULL,
  company VARCHAR(255),
  user_type VARCHAR(10) DEFAULT 'guest'
);